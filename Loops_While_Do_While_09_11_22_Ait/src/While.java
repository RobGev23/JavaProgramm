public class While {
    public static void main(String[] args) {

        int a = 12;
        int i = 0;

        while (i < 3){
            System.out.println(a);
            i++;
        }
        System.out.println("--------------------------------");

        int j = 0;
        while (j < 5){
            System.out.println("HI");
            j++;
        }
        System.out.println("-------------------------------");

        int k = 5;
        while (k > 0){
            System.out.println(k);
            k--;
        }
    }
}
